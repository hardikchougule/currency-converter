for (select of dropdown) {
    for (code in countryList) {
        let newoption = document.createElement('option');
        newoption.innerText = code;
        newoption.value = code;
        if (select.name == 'selectfrom' && code == 'INR') {
            newoption.selected = "selected";
        }
        if (select.name == 'selectTo' && code == 'USD') {
            newoption.selected = "selected";
        }
        select.append(newoption);

        select.addEventListener("change", (evt) => {
            updateFlag(evt.target);
        });

    }
}

const updateFlag = (element) => {
    let currCode = element.value;
    let countryCode = countryList[currCode];
    let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png`;
    let img = element.parentElement.querySelector("img");
    img.src = newSrc;
};



async function changerate() {
    let inpval = document.querySelector('#tocon')
    let value = inpval.value;
    if (value === '' || value < 1) {
        value = 1;
        inpval.value = 1;
    }
    let URL = `${main_url}/${fromsel.value.toLowerCase()}/${tosel.value.toLowerCase()}.json`;
    outprint.innerHTML="Getting data...";
    let response = await fetch(URL);
    let data = await response.json();
    let rate = data[tosel.value.toLowerCase()];
    let finalvalue = value*rate;
    outprint.innerHTML=finalvalue;
    console.log(data);
    datei.innerHTML=data["date"];  
}


btn.addEventListener('click', changerate);

